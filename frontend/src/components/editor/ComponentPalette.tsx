const categoryLabels: Record<string, string> = {
  basic: 'Basic',
  layout: 'Layout',
  media: 'Media',
  content: 'Content',
  forms: 'Forms',
  integration: 'Integration',
};

interface DraggableComponentProps {
  component: ComponentDefinition;
}

const DraggableComponent: React.FC<DraggableComponentProps> = ({ component }) => {
  const { attributes, listeners, setNodeRef, isDragging } = useDraggable({
    id: `palette-${component.type}`,
  });

  return (
    <div
      ref={setNodeRef}
      {...listeners}
      {...attributes}
      className={`flex items-center gap-2 p-2 rounded cursor-grab hover:bg-gray-100 transition-colors ${
        isDragging ? 'opacity-50' : ''
      }`}
    >
      <span className="text-gray-500">{component.icon}</span>
      <span className="text-sm text-gray-700">{component.label}</span>
    </div>
  );
};

const ComponentPalette: React.FC = () => {
  const [expandedCategories, setExpandedCategories] = useState<Record<string, boolean>>({
    basic: true,
    layout: true,
    media: false,
    content: false,
    forms: false,
    integration: false,
  });

  const toggleCategory = (category: string) => {
    setExpandedCategories((prev) => ({
      ...prev,
      [category]: !prev[category],
    }));
  };

  const groupedComponents = components.reduce((acc, comp) => {
    if (!acc[comp.category]) {
      acc[comp.category] = [];
    }
    acc[comp.category].push(comp);
    return acc;
  }, {} as Record<string, ComponentDefinition[]>);

  return (
    <div className="w-64 bg-white border-r border-gray-200 overflow-y-auto">
      <div className="p-4 border-b border-gray-200">
        <h2 className="font-semibold text-gray-900">Components</h2>
        <p className="text-xs text-gray-500 mt-1">Drag to add to page</p>
      </div>

      <div className="p-2">
        {Object.entries(groupedComponents).map(([category, comps]) => (
          <div key={category} className="mb-2">
            <button
              onClick={() => toggleCategory(category)}
              className="w-full flex items-center justify-between p-2 text-sm font-medium text-gray-700 hover:bg-gray-50 rounded"
            >
              {categoryLabels[category]}
              <svg
                className={`w-4 h-4 transition-transform ${
                  expandedCategories[category] ? 'rotate-180' : ''
                }`}
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M19 9l-7 7-7-7"
                />
              </svg>
            </button>

            {expandedCategories[category] && (
              <div className="ml-2 space-y-1">
                {comps.map((comp) => (
                  <DraggableComponent key={comp.type} component={comp} />
                ))}
              </div>
            )}
          </div>
        ))}
      </div>
    </div>
  );
};

export default ComponentPalette;
